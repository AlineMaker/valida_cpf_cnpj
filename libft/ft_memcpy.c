#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char	*p_dest;
	unsigned char	*p_src;

	p_dest = (unsigned char *)dest;
	p_src = (unsigned char *)src;
	if (p_dest == NULL && p_src == NULL)
		return (dest);
	while (n--)
		*p_dest++ = *p_src++;
	return (dest);
}
